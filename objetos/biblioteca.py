# clase para gestionar prestamos

class Prestamo:
    def __init__(self, usuario, material, dias_prestados, fecha_limite):
        self.__usuario = usuario
        self.__material = material
        self.__dias_prestados = dias_prestados
        self.__fecha_limite = fecha_limite

# getters 
def get_usuario(self):
    return self.__usuario 
def get_material(self):
    return self.__material 
def get_dias_prestados(self):
    return self.__dias_prestados 



